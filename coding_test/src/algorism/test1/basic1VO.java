public class basic1VO {
	private String name;
	private int no;
	public basic1VO(String name, int no) {
		this.name = name;
		this.no = no;
	}
	public basic1VO() {
		super();
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getNo() {
		return no;
	}
	public void setNo(int no) {
		this.no = no;
	}
	
	
	
}
